let p: number = 4;

console.log("Initial value of x:", p); // Output: Initial value of p: 4

// Compound assignment operators
p += 3; // Equivalent to: p = p + 3;
console.log("After addition (p += 3):", p); // Output: After addition (p += 3): 7

p -= 2; // Equivalent to: p = p - 2;
console.log("After subtraction (p -= 2):", p); // Output: After subtraction (p -= 2): 5

p *= 6; // Equivalent to: p = p * 6;
console.log("After multiplication (p *= 6):", p); // Output: After multiplication (p *= 6): 30

p /= 3; // Equivalent to: p = p / 3;
console.log("After division (p /= 3):", p); // Output: After division (p /= 3): 10
