function updateVariable() {
    var x = 5; // Declaring a variable using let and assign an initial value
    console.log("Initial value of x:", x); // Output: Initial value of x: 5
    // Update the value of x
    x = 10;
    console.log("Updated value of x:", x); // Output: Updated value of x: 10
}
// Calling the function
updateVariable();
